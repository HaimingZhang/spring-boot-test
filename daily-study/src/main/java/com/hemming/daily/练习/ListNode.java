public class ListNode {
    public int val;

    public ListNode next;

    public ListNode(int _val) {
        this.val = _val;
    }

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }

    public ListNode getNext() {
        return next;
    }

    public void setNext(ListNode next) {
        this.next = next;
    }
}
